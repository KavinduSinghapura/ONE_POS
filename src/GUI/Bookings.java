    private void loadBookings(){
        
        try {
            
            ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `room_bookings` INNER JOIN `customer` ON `room_bookings`.`customer_id` = `customer`.`id` INNER JOIN `rooms` ON `room_bookings`.`rooms_id` = `rooms`.`id` INNER JOIN `room_status` ON `room_bookings`.`room_status_id` = `room_status`. `id`");
           
            DefaultTableModel modal = (DefaultTableModel) jTable1.getModel();
            modal.setRowCount(0);
            
            while(resultSet.next()){
                
                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("room_bookings.id"));
                vector.add(resultSet.getString("customer.mobile"));
                vector.add(resultSet.getString("rooms.name"));
                vector.add(resultSet.getString("checking_date"));
                vector.add(resultSet.getString("checkout_date"));
                vector.add(resultSet.getString("days"));
                vector.add(resultSet.getString("price"));
                vector.add(resultSet.getString("customer.fname"));
                vector.add(resultSet.getString("room_status.name"));
                
                modal.addRow(vector);
                
            }
            
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }
    
